<p>Oyez jeunes gens, nous vous annonçons ici l'ouverture d'un type d'Event assez spécial, que vous ne trouverez que sur Odyssée : La Bataille pour le Col de Forgefer.</p>
<p>Cet event se compose d'un match a 20c20, ou 30c30, horde contre alliance, sur une zone customisée par les soins de Maz, Balrogard, et moi même.</p>
<p>La horde a décidé d'attaquer Forgefer par l'aéroport Gnome, afin de pénétrer par les hauteurs de la ville ! Le Col de Balderune sera donc le théâtre des affrontements rangés entre le corps expéditionnaire de la horde, et l'armée de secours de l'alliance. 2 armées de PNJ se font face dans un décors de ruine et de siège, afin de débloquer la situation, les joueurs devront vaincre l'équipe ennemie, tout du moins son Champion.</p>
