<a href="#" id="titrefaq4">
               <h4>Le joueur X m’a insulté.</h4>
</a>
                              <div class="texte-faq" id="textefaq4">
<p>En cas de flood / insultes lourdes, homophobes, racistes  etc… De très lourdes sanctions peuvent être prises à l’encontre du fautif, et  ce pour le bien de la communauté. Référez vous <a href="reglement.php">au barème de sanctions du serveur</a> pour plus  d’informations. Pour les autres désagréments, il existe la commande /ignore nomdujoueur  qui empêche le joueur concerné de vous contacter. Bien sur, en cas de  harcèlement via d’autres personnages, vous pouvez avoir recours aux Maîtres du Jeu. Il est nécessaire de pouvoir présenter le plus de «&nbsp;preuves&nbsp;»  possibles, à savoir des Screenshot du moment et des propos énoncés, pour que  l’affaire soit menée le plus rapidement et efficacement possible.</p>
<br/> <br/> </div>

<a href="#" id="titrefaq5">
               <h4>Le joueur X pollue le canal World</h4>
</a> 	
                              <div class="texte-faq" id="textefaq5">
                                <p>Il y a en  quasi permanence un membre de l’équipe connecté sur son personnage InGame et  apte à surveiller le canal World d’une ou des deux factions, malheureusement,  il arrive que certains floodeurs passent entre les mailles de nos filets. Si un  joueur pollue / insulte / flood via le canal world, et qu’aucun Maître du Jeu  ne semble intervenir, n’hésitez pas à prendre un Screenshot de la conversation  (en cas d’insultes / propos racistes &amp; homphobes) et de reporter le tout  sur le forum dans la partie adéquate (<a href="http://forum.odyssee-serveur.com/forum/27-problemes-de-comportements/">http://forum.odyssee-serveur.com/forum/27-problemes-de-comportements/</a>), un membre de l’équipe se chargera  de sanctionner le fautif.</p> <br/> <br/>
</div>  
<a href="#" id="titrefaq6">

                              <h4>Je viens de voir X utiliser un  logiciel de Hack&nbsp;!</h4> </a> 	
<div class="texte-faq" id="textefaq6">
Comme vous le savez certainement, certains joueurs aiment utiliser de tels logiciels afin de contourner les règles du jeu. Si il vous arrive d’en prendre un sur le fait (notamment pour Speed/Fly/TpHack), n’hésitez pas à effectuer immédiatement une requête InGame. Il se peut qu’un Maître du Jeu ne soit pas connecté ou disponible à ce moment là, effectuez un maximum de Screenshot afin de pouvoir déposer votre plainte sur le forum (Attention, en cas de Speedhack, les Screenshots ne servent à rien).
<br/> <br/> </div>

