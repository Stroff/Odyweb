                          <a href="#" id="titrefaq3">
								<h4>Puis-je changer de race, de classe ou de faction ? </h4>
                             </a> 
                   
                           <div class="texte-faq" id="textefaq3">
                            	<ul>
                                	<li>Si votre personnage était dans la horde, et que vous voulez le passer en alliance, (ou vice-versa), cela est possible. 
                                Mais uniquement dans le cadre de la récupération. Une fois que votre personnage aura été récupéré,<strong> il sera impossible de changer de faction.</strong></li>
                               		<li><strong>Vous ne pouvez changer la classe de votre personnage</strong>. Si vous demandez la récupération d’un chaman, vous devrez créer un chaman.</li>
                               		<li><strong>Vous pouvez choisir une race différente de celle de votre personnage d’origine. </strong>Attention cependant à choisir une race compatible avec votre classe de personnage. </li>
                               </ul>
                            <br/> <br/> <br/> 
                           </div>  
                            <a href="#" id="titrefaq1">
                               <h4>     Je veux récupérer mon personnage, comment ça marche ?       </h4>                           
                                 </a> 
                      
                           <div class="texte-faq" id="textefaq1">
                             <ul>
                               <li>Nous proposons aux joueurs de récupérer leurs personnages crées sur d’autres serveurs via un système de récupération automatique. </li>
                               <li>Les récupérations sont possibles<strong> uniquement pour des personnages de level 60 ou supérieur</strong>, et sont entièrement gratuites. </li>
                               <li>Chaque personnage ne peut être récupéré qu’une fois.</li>
                               <li>Il s’agit en fait d’une copie de votre personnage,<strong> il ne sera donc pas identique aux screens que vous aurez fournis</strong>, que ce soit au niveau de l’équipement ou de l’apparence. </li>
                             </ul>
                             <br/> <br/> <br/> 
                           </div>   
                          
                        <a href="#" id="titrefaq6">
                    <h4>  Comment faire ma demande de récupération ? </h4>
                              
                             </a> 
 
                           <div class="texte-faq" id="textefaq6">
                           <br/>
                                 <ol>
                                  
										<li> Créez votre compte sur le site si ce n'est déjà fait, et connectez vous en jeu pour créer un personnage de la classe de votre personnage à récupérer (race et faction au choix)</li>
										<li>Lancer votre demande de récupération en suivant les consignes indiquées et attendez sa validation (ou non). <br/>
                                        <strong>Vous recevrez un Email dès que le statut aura changé</strong>, lisez-le, un MJ vous a peut-être laissé un message(il suffit de faire "répondre" en cas de besoin pour envoyer un mail au MJ, à faire seulement s'il vous demande de lui répondre)</li>
											<li> Si la demande est validée, connectez vous en jeu, ciblez-vous, et tapez la commande .récup</li>
											<li>Ne vous déconnectez pas avant d'avoir récupéré vos objets au courrier (sinon ils disparaitront,il faudra faire une requête et attendre qu'elle soit traitée.
                                             Mieux vaut donc récupérer votre courrier de suite, et déconnecter après)</li>
								</ol>
                                
								<br/> <br/> <br/> 
                               
                           </div>  
                        <a href="#" id="titrefaq7">
                <h4>   Ma récup a été validée, mais elle ne fonctionne pas. </h4>
                              
                             </a> 
                 
                           <div class="texte-faq" id="textefaq7">
                                <ul>
                                	<li><strong>Assurez-vous d’avoir bien ciblé votre personnage avant de taper la commande .récup</strong></li>
									<li>Si vous avez un message d’erreur disant que la récupération n’a peut-être pas été validée, vérifiez dans votre suivi de récupération qu’elle a bien été validée. 
                                    <br/>Si c’est le cas, contactez un maitre du jeu, soit sur le forum, soit par le sytème de requête en jeu</li>
									<li>Si vous avez un message d’erreur disant que le personnage n’est pas de la bonne classe, c’est que la classe du personnage que vous avez créé ne correspond pas à celle que vous avez indiqué dans le formulaire. Donc si vous avez fait une erreur dans le formulaire, contactez un maitre du jeu pour qu’il le modifie.
                                    <br/>
                                    Si vous vous êtes trompé dans la création du personnage, créez un nouveau personnage (de la bonne classe) et contactez un maitre du jeu pour qu’il re-valide votre demande.</li>
									<li>Si vous avez un message d’erreur disant que le personnage n’est pas de la bonne race, cela signifie que la race du personnage créé ne correspond pas à celle du formulaire.
                                        <br/>Contactez un maitre du jeu pour qu’il corrige la demande et la re-valide.</li>
								<br/>
                                </ul>
                                <br/> <br/> <br/> 

                           </div>  
                          
                       <a href="#" id="titrefaq8">
                <h4>    Ma demande de récupération a été refusée, je ne comprends pas pourquoi ? </h4>
                             </a> 
                         
                           <div class="texte-faq" id="textefaq8">
                           		<ul>
                                	<li>S’il est indiqué dans votre suivi : « refus, manque screen » cela signifie que vous n’avez pas fourni tous les screens demandés. 
                                    <br/>Lisez également vos emails, il est possible qu’un maitre du jeu vous ai envoyé une précision sur le (les) screen(s) manquant(s)</li>
									<li>S’il est indiqué : « refus, fake récup », cela peut signifier que le personnage dont vous demandez la récupération a déjà été récupéré sur ce serveur, ou que les screens fournis ont été modifiés (entre autre). 
                                    <br/>Pour information, ce motif de refus peut entrainer un ban de votre compte pour 7 jours.</li>
									<li>S’il est indiqué « en attente de réponse du joueur », lisez vos emails. Un maitre de jeu vous a posé une question et attends une réponse de votre part.</li>
									<li>S’il est indiqué « doublon » c’est qu’une 1ere demande a déjà été validée . 
                                    <br/>Si vous avez un soucis pu si vous avez fait une erreur dans le 1er formulaire, lisez le paragraphe « ma récup a été validée, mais elle ne fonctionne pas ».</li>
								</ul>
                                <br/> <br/> <br/> 
                           </div>  
                       
