                            <a href="#" id="titrefaq2">    <h4>
                                  Que récupére-t-on au juste ?
                                  
                                </h4></a>
                             
                           <div class="texte-faq" id="textefaq2">
                           <ul>
                               <li>Cela dépend du niveau (level) de votre personnage et de son niveau d’équipement. </li>
                               <li><strong>Votre personnage est de niveau 60 à 69 :</strong> vous récupérerez vos métiers jusqu’au niveau 375 maximum, votre niveau de monte, un équipement de niveau T2, 2000 pièces d’or, 1 monture terrestre et 1 monture volante si vous avez la compétence. </li>
                               <li><strong>Votre personnage est de niveau 70 à 79 : </strong>vous récupérerez vos métiers jusqu’au niveau 375 maximum, votre niveau de monte, un équipement de niveau T3.5, T4, T5, S1 ou S2 (selon votre équipement), 2000 pièces d’or, 1 monture terrestre et 1 monture volante si vous avez la compétence </li>
                               <li><strong>Votre personnage est de niveau 80 :</strong> vous récupérerez vos métiers jusqu’au niveau 375 maximum, votre niveau de monte, un équipement de niveau 78 (bleu), 4000 pièces d’or, 1 monture terrestre et 1 monture volante si vous avez la compétence </li>
                               <li><strong>Dans tous les cas : </strong>Enchantements et gemmes non récupérés / Réputations non récupérées / vol par temps froid non récupéré </li>
                             </ul>
<br/> <br/> <br/> 
                           </div>  
         
                   
                          
