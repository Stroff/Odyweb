           <h1>Newsletter Odyssée – Il y a de la houle !</h1>


Joueurs de divers horizons, passionnés de MMORPG, addicts de WoW, bonjour. <br/> <br/> 

Odyssée, serveur blizzlike à votre service depuis plus de deux ans, vous invite à venir découvrir ses dernières nouveautés ! Ces dernières semaines, nous avons travaillé d'arrache-pied pour fournir à toutes et à tous la qualité de jeu qu'un serveur privé se doit d'offrir à ses joueurs.
 Aussi était-il temps de faire un récapitulatif des nouveautés qui ont secoué notre communauté.<br/> <br/> 
<h2>1/Sur la scène de l’émulation WOW</h2><br/> 
<h3>Top votes : un debut de justice ?</h3><br/> 
Vous vous en êtes peut-être rendus compte, l'effondrement de notre place sur RPG-P était dû aux bots votes mis en place par les serveurs privés eux-mêmes pour remonter aux classements. La différence avec un bot vote destiné à gagner des points boutique?
<br/> 
La où les bots votes pour Odyssée (largement combattus par notre équipe) ne touchent qu'à nos pody, les bots votes mis en places par les team eux-mêmes permettent aussi de contourner le captcha RPG-Paradize.
Nous espérons que ce redressement continuera avec les autres sites coupables, et que cela n'est pas dû uniquement à la perte de la première place qu'avait perdue un certain serveur, ce qui aurait poussé la direction de RPG P à agir par amitié... Nous espérons que le leader en question, qui ne se prive pas de ces manœuvres non plus, subira en toute justice le même sort que ses collègues, comme ceux dont on trouve facilement les bots sur internet qui contournent le captcha RPG P.
<br/> <br/> 
Notre team a toujours souhaité rester honnête, pour nous aider, que faire?
Renouveler d'attention et voter sur prg paradize pour que nous remontions à la place que nous méritons! En espérant que la solidarité et l’honnêteté dont nous avons fait preuve paiera enfin.
<br/> <br/> 
